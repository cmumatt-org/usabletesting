/**
 * Adapted from: https://stackoverflow.com/questions/44551132/
 *
 * Accepts an array of numbers and returns the sum of the numbers.
 *
 * @param values An array of finite numbers
 * @returns The maximum number in the array
 */
export function maxOfArray(values: any): number {
  return Math.max(values);
}

